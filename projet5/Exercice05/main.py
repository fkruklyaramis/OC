def sum(a, b):
    """Add two numbers and return the result.
    This function takes two numbers as input and returns their sum.
    Args:
        a: First number to add
        b: Second number to add
    Returns:
        The sum of a and b
    """

    return a + b
 
def subtraction(a, b):
    """
    Subtract two numbers.
    Args:
        a: The minuend.
        b: The subtrahend.
    Returns:
        The difference between a and b.
    """

    return a - b
